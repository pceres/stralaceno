04 Aprile 2007 – REAL SANT’ANDREA- CAPOSELE = 1 – 1( Al 23’ p.t. Di Guglielmo G., 33’ p.t. Cibellis)
Note: Al 23’p.t. espulso  di Gugliemo G. per somma di ammonizioni, al 44’s.t. espulso Grasso per somma di ammonizioni. Terreno appesantito dalla pioggia fitta e costante della giornata - Spettatori della variante circa 50, spettatori degli spalti aperti 150.
&nbsp;
Eccoci!!! La partita della stagione, il big match che dal 2 febbraio doveva essere giocato, ma che per tanti motivi è stato sempre rimandato. Oggi pomeriggio si è disputata una delle più belle partite che la terza categoria abbia mai potuto avere. Si sono affrontate le due squadre più forti del girone G.. Il Real e il Caposele hanno offerto al numeroso pubblico una partita che molti ricorderanno per tanti motivi.
&nbsp;
Dal fischio iniziale si capisce che la squadra, che affrontano i nostri leoni, non è la solita della categoria, anzi i nostri avversari sono tecnicamente molto preparati e sembrano aver assimilato a memoria gli schemi dettati dal loro trainer.
I primi minuti si sviluppano con azioni nel mezzo del campo e le due squadre danno l’impressione di voler guadagnare i tre punti senza però rischiare e sembra che da un momento all’altro una delle due squadre possa passare in vantaggio.
Dopo alcune, purtroppo solite, sviste arbitrali, che regalano pericolose punizioni e penalizzano con ammonizioni i nostri leoni, il REAL inizia a creare problemi dalle fasce. Infatti dopo un’azione travolgente di FRECCIA NERA CECCHINI’ che porge al centro dell’area un traversone mal sfruttato dai nostri attaccanti. Da un’ altra azione, questa volta, dalla fascia destra SUPERMAN Gaetano prende palla, alza la testa e lascia partire una cannonata tanto forte quanto precisa che si insacca alle spalle dell’ incolpevole portiere del Caposele, è l’ 1 a 0.
La gioia è tanta e l’ adrenalina annebbia la razionalità di Gaetano, infatti dopo aver realizzato lo splendido vantaggio istintivamente toglie la maglietta… l’arbitro non può far altro che applicare il regolamento, quindi tira fuori il secondo giallo e lo espelle. Dalla gioia alla tristezza nel giro di pochi secondi.
Nonostante l’uomo in meno e con ancora più di 60 minuti da giocare, la squadra non si perde d’animo e risponde agli attacchi avversari con una solida difesa, che per la prima volta dall’inizio di questa stagione è obbligata agli straordinari. Purtroppo, dopo pochi minuti dall’espulsione di Gaetano, il Real perde un’altra pedina importante dello scacchiere: Carbone, il quale, dopo uno scatto, risente del problema al quadricipite, che lo aveva tenuto fuori nelle passate partite, ed è costretto ad uscire. Persi nel giro di 5 minuti i perni del centrocampo e dell’attacco, il mister è costretto a stravolgere i ruoli posizionando Angioletto Cocchiarella nella zona d’attacco ed il piccolo grande Amorosi Giuseppe nel ruolo che era di Gaetano. Prima della fine del primo tempo, da ricordare una brillante azione degli avversari che mette in evidenzia la buona vena di oggi e la reattività della nostra SARACINESCA Lanza, il quale si produce in una bella parata di piede su un tiro insidiosissimo. Fischio finale che sancisce la fine del primo tempo.
&nbsp;
Al rientro dagli spogliatoi il Caposele entra con un piglio diverso in campo e cerca di sfruttare al massimo la superiorità numerica. Mettono in crisi la nostra squadra con continui rilanci ed aperture, ma i nostri leoni non sono assopiti e tirano fuori gli artigli, ricominciando a giocare come sanno, senza però creare seri grattacapi al loro portiere.
Il pareggio è nell’aria e, dopo un’altra spettacolare parata di Lanza, che toglie dall’incrocio un colpo di testa a botta sicura, gli avversari capitalizzano la mole di gioco prodotta sfruttando l’ unica disattenzione difensiva pareggiano su corta respinta del nostro numero 12.
Il Caposele galvanizzato dal gol continua a macinare gioco e per due volte va vicino al raddoppio, nella prima occasione il palo salva Lanza e nella seconda il tiro del loro attaccante si deposita di qualche centimetro al lato.
Dopo altri infortuni e relative sostituzioni dei nostri, la partita si avvia mestamente alla fine, ma l’orgoglio del REAL non ha limiti, negli ultimi 5 minuti i leoni si riversano in attacco nonostante l’uomo in meno e si creano tre palle gol nitidissime che però non vengono trasformate in segnature e non regalano la gioia ai tanti tifosi.
[1] Calcio d’angolo, respinta dei difensori, palla vagante e gran botta di Robby CARECA Pugliese, il tiro sorvola il montante di pochi centimetri.
[2] Lancio di Camerlimgo per Pugliese, fallo del loro difensore al limite dell’area, ma l’arbitro concede la regola del vantaggio perchè la palla arriva tra i piedi della corrente Donato CASSANO Di Guglielmo, che a tu per tu non riesce ad insaccare ma si fa deviare il tiro in corner.
[3] Dall’angolo seguente, Gallucci defilato sul secondo palo stoppa la palla e lascia partire un tiro sbilenco che diventa un assist d’oro per Antonio PICCHIO Pugliese, che gira di testa sotto porta, ma alza incredibilmente sopra la traversa. Mamma mia che erroraccio… ma non importa, i primi a soffrire sono i nostri leoni, che nonostante tutto escono al triplice fischio finale tra gli applausi dei tifosi.
